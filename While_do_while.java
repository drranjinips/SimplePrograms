package simple_programs;

public class While_do_while {

	public static void main(String[] args) {
		//DEMONSTRATION OF WHILE LOOP TO PRINT THE NUMBERS FROM 1 TO 10
		int i=1;
		while(i<=10)
		{System.out.println(i);
		 i++;
		}
		//DEMONSTRATION OF WHILE LOOP TO PRINT THE MULTIPLICATION TABLE OF 5
		
		int j=1;
		int num = 5;
		while(j<=10)
		{System.out.println(j + "*" + num + "="  + (j*num));
		 j++;
		}

				
	}

}
